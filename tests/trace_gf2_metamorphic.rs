#[test]
fn popcount_parity_is_additive_under_xor() {
    // popcount(a ^ b) ≡ popcount(a) + popcount(b)  (mod 2)
    for &len in BITVEC_LENS {
        for seed in 0..40u64 {
            let mut rng = Rng::new(seed ^ 0x99 ^ (len as u64) << 8);
            let a = gen_bitvec(&mut rng, len, 24);
            let b = gen_bitvec(&mut rng, len, 24);
            let lhs = a.xor(&b).count_ones() % 2;
            let rhs = (a.count_ones() + b.count_ones()) % 2;
            assert_eq!(lhs, rhs, "xor parity broke (len={len}, seed={seed})");
        }
    }
}

#[test]
fn popcount_under_xor_is_hamming_bounded() {
    // |popcount(a) - popcount(b)| <= popcount(a^b) <= popcount(a)+popcount(b)
    for &len in BITVEC_LENS {
        for seed in 0..32u64 {
            let mut rng = Rng::new(seed ^ 0x42 ^ (len as u64) << 8);
            let a = gen_bitvec(&mut rng, len, 24);
            let b = gen_bitvec(&mut rng, len, 24);
            let pa = a.count_ones();
            let pb = b.count_ones();
            let pc = a.xor(&b).count_ones();
            assert!(
                pc <= pa + pb,
                "upper Hamming bound (len={len}, seed={seed})"
            );
            assert!(
                pc >= pa.abs_diff(pb),
                "lower Hamming bound (len={len}, seed={seed})"
            );
        }
    }
}

#[test]
fn count_ones_equals_ones_iterator_length() {
    for &len in BITVEC_LENS {
        for seed in 0..24u64 {
            let mut rng = Rng::new(seed ^ 0x5B ^ (len as u64) << 8);
            let v = gen_bitvec(&mut rng, len, 24);
            assert_eq!(
                v.count_ones(),
                v.ones().count(),
                "count_ones vs ones() (len={len}, seed={seed})"
            );
        }
    }
}

#[test]
fn ones_iterator_is_strictly_ascending_and_in_range() {
    for &len in BITVEC_LENS {
        for seed in 0..24u64 {
            let mut rng = Rng::new(seed ^ 0x6D ^ (len as u64) << 8);
            let v = gen_bitvec(&mut rng, len, 24);
            let mut prev: Option<usize> = None;
            for idx in v.ones() {
                assert!(idx < len, "ones() index {idx} >= len {len}");
                assert!(v.get(idx), "ones() yielded a clear bit {idx}");
                if let Some(p) = prev {
                    assert!(p < idx, "ones() not strictly ascending: {p} !< {idx}");
                }
                prev = Some(idx);
            }
        }
    }
}

