#[test]
fn reduce_is_deterministic() {
    for &(rows, cols, density) in MATRIX_CASES {
        for seed in 0..8u64 {
            let mut rng = Rng::new(seed ^ 0x1234 ^ (rows as u64) << 16 ^ (cols as u64));
            let m = gen_matrix(&mut rng, rows, cols, density);
            let r1 = m.reduce();
            let r2 = m.reduce();
            for j in 0..cols {
                assert_eq!(
                    r1.matrix.column(j),
                    r2.matrix.column(j),
                    "reduce non-deterministic at col {j} ({rows}x{cols}, seed={seed})"
                );
            }
            assert_eq!(r1.pivot_map, r2.pivot_map, "pivot_map non-deterministic");
        }
    }
}

#[test]
fn reduce_preserves_dimensions() {
    for &(rows, cols, density) in MATRIX_CASES {
        for seed in 0..6u64 {
            let mut rng = Rng::new(seed ^ 0x55AA ^ (rows as u64) << 16 ^ (cols as u64));
            let m = gen_matrix(&mut rng, rows, cols, density);
            let reduced = m.reduce();
            assert_eq!(reduced.matrix.rows(), rows, "rows changed");
            assert_eq!(reduced.matrix.cols(), cols, "cols changed");
            assert_eq!(reduced.pivot_map.len(), rows, "pivot_map size != rows");
        }
    }
}

#[test]
fn reduced_columns_have_distinct_pivots() {
    // The defining property of a reduced matrix: no two non-zero columns
    // share a pivot (lowest/highest set bit).
    for &(rows, cols, density) in MATRIX_CASES {
        for seed in 0..8u64 {
            let mut rng = Rng::new(seed ^ 0xBEEF ^ (rows as u64) << 16 ^ (cols as u64));
            let m = gen_matrix(&mut rng, rows, cols, density);
            let reduced = m.reduce();
            let mut seen = BTreeSet::new();
            for j in 0..cols {
                if let Some(p) = reduced.matrix.column_pivot(j) {
                    assert!(
                        seen.insert(p),
                        "duplicate pivot {p} at col {j} ({rows}x{cols}, seed={seed})"
                    );
                }
            }
        }
    }
}

#[test]
fn reduce_is_idempotent() {
    // Reducing an already-reduced matrix must not change any column pivot.
    for &(rows, cols, density) in MATRIX_CASES {
        for seed in 0..8u64 {
            let mut rng = Rng::new(seed ^ 0xF00D ^ (rows as u64) << 16 ^ (cols as u64));
            let m = gen_matrix(&mut rng, rows, cols, density);
            let once = m.reduce();
            let twice = once.matrix.reduce();
            for j in 0..cols {
                assert_eq!(
                    once.matrix.column_pivot(j),
                    twice.matrix.column_pivot(j),
                    "reduce not idempotent at col {j} ({rows}x{cols}, seed={seed})"
                );
            }
            // A second reduction of a reduced matrix moves nothing at all.
            for j in 0..cols {
                assert_eq!(
                    once.matrix.column(j),
                    twice.matrix.column(j),
                    "reduced column {j} mutated by re-reduction"
                );
            }
        }
    }
}

#[test]
fn pivot_map_round_trips_with_column_pivot() {
    for &(rows, cols, density) in MATRIX_CASES {
        for seed in 0..8u64 {
            let mut rng = Rng::new(seed ^ 0xACE ^ (rows as u64) << 16 ^ (cols as u64));
            let m = gen_matrix(&mut rng, rows, cols, density);
            let reduced = m.reduce();

            // Every recorded (row -> col) maps back: that column's pivot is `row`.
            let mut mapped_cols = BTreeSet::new();
            for (row, entry) in reduced.pivot_map.iter().enumerate() {
                if let Some(col) = *entry {
                    assert!(col < cols, "pivot_map points to out-of-range col {col}");
                    assert_eq!(
                        reduced.matrix.column_pivot(col),
                        Some(row),
                        "pivot_map[{row}]={col} but column_pivot({col}) disagrees"
                    );
                    assert!(
                        mapped_cols.insert(col),
                        "column {col} claimed by two pivot rows"
                    );
                }
            }
            // Conversely, every non-zero reduced column is registered.
            for j in 0..cols {
                if let Some(p) = reduced.matrix.column_pivot(j) {
                    assert_eq!(
                        reduced.pivot_map[p],
                        Some(j),
                        "non-zero column {j} (pivot {p}) missing from pivot_map"
                    );
                }
            }
        }
    }
}

#[test]
fn zero_columns_stay_zero_through_reduction() {
    for &(rows, cols, density) in MATRIX_CASES {
        for seed in 0..6u64 {
            let mut rng = Rng::new(seed ^ 0x0C0 ^ (rows as u64) << 16 ^ (cols as u64));
            let mut m = gen_matrix(&mut rng, rows, cols, density);
            // Force a couple of columns to zero.
            let zeroed: Vec<usize> = (0..cols).step_by(3).collect();
            for &j in &zeroed {
                *m.column_mut(j) = BitVec::zeros(rows);
            }
            let reduced = m.reduce();
            for &j in &zeroed {
                assert!(
                    reduced.matrix.column(j).is_zero(),
                    "zero column {j} became non-zero after reduction"
                );
                assert_eq!(reduced.matrix.column_pivot(j), None);
            }
        }
    }
}

// ---------------------------------------------------------------------------
// Rank invariance — `reduce` is built only from elementary column XORs, so the
// rank (count of non-zero reduced columns) must be invariant under operations
// that span the same column space.
// ---------------------------------------------------------------------------

#[test]
fn rank_is_invariant_under_column_permutation() {
    for &(rows, cols, density) in MATRIX_CASES {
        for seed in 0..10u64 {
            let mut rng = Rng::new(seed ^ 0x9E9E ^ (rows as u64) << 16 ^ (cols as u64));
            let m = gen_matrix(&mut rng, rows, cols, density);
            let base_rank = rank(&m);

            // Fisher-Yates shuffle the columns.
            let mut cols_vec = columns(&m);
            for i in (1..cols_vec.len()).rev() {
                let j = rng.below(i + 1);
                cols_vec.swap(i, j);
            }
            let permuted = BoundaryMatrix::from_columns(rows, cols_vec);
            assert_eq!(
                rank(&permuted),
                base_rank,
                "rank changed under column permutation ({rows}x{cols}, seed={seed})"
            );
        }
    }
}

#[test]
fn rank_is_invariant_under_elementary_column_xor() {
    // XOR-ing column `src` into a distinct column `dst` is an elementary
    // column operation: it preserves the column space, hence the rank.
    for &(rows, cols, density) in MATRIX_CASES {
        if cols < 2 {
            continue;
        }
        for seed in 0..10u64 {
            let mut rng = Rng::new(seed ^ 0x3711 ^ (rows as u64) << 16 ^ (cols as u64));
            let m = gen_matrix(&mut rng, rows, cols, density);
            let base_rank = rank(&m);

            let mut mutated = m.clone();
            let dst = rng.below(cols);
            let mut src = rng.below(cols);
            if src == dst {
                src = (src + 1) % cols;
            }
            mutated.xor_columns(dst, src);
            assert_eq!(
                rank(&mutated),
                base_rank,
                "rank changed under elementary column XOR ({rows}x{cols}, seed={seed})"
            );
        }
    }
}

#[test]
fn appending_a_zero_column_does_not_change_rank() {
    for &(rows, cols, density) in MATRIX_CASES {
        for seed in 0..8u64 {
            let mut rng = Rng::new(seed ^ 0x2222 ^ (rows as u64) << 16 ^ (cols as u64));
            let m = gen_matrix(&mut rng, rows, cols, density);
            let base_rank = rank(&m);

            let mut cols_vec = columns(&m);
            cols_vec.push(BitVec::zeros(rows));
            let extended = BoundaryMatrix::from_columns(rows, cols_vec);
            assert_eq!(
                rank(&extended),
                base_rank,
                "zero column raised rank ({rows}x{cols}, seed={seed})"
            );
        }
    }
}

#[test]
fn duplicating_a_column_does_not_raise_rank() {
    // Appending a copy of an existing column adds a linear dependency, so the
    // rank can only stay the same (it never rises).
    for &(rows, cols, density) in MATRIX_CASES {
        if cols == 0 {
            continue;
        }
        for seed in 0..8u64 {
            let mut rng = Rng::new(seed ^ 0x4D4D ^ (rows as u64) << 16 ^ (cols as u64));
            let m = gen_matrix(&mut rng, rows, cols, density);
            let base_rank = rank(&m);

            let mut cols_vec = columns(&m);
            let dup = rng.below(cols);
            cols_vec.push(cols_vec[dup].clone());
            let extended = BoundaryMatrix::from_columns(rows, cols_vec);
            assert_eq!(
                rank(&extended),
                base_rank,
                "duplicated column changed rank ({rows}x{cols}, seed={seed})"
            );
        }
    }
}

