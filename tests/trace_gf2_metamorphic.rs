#[test]
fn pivot_and_highest_bit_match_ones_extremes() {
    for &len in BITVEC_LENS {
        for seed in 0..40u64 {
            let mut rng = Rng::new(seed ^ 0x2F ^ (len as u64) << 8);
            let v = gen_bitvec(&mut rng, len, 20);
            let ones: Vec<usize> = v.ones().collect();
            assert_eq!(
                v.pivot(),
                ones.first().copied(),
                "pivot != min(ones) (len={len}, seed={seed})"
            );
            assert_eq!(
                v.highest_bit(),
                ones.last().copied(),
                "highest_bit != max(ones) (len={len}, seed={seed})"
            );
            if let (Some(lo), Some(hi)) = (v.pivot(), v.highest_bit()) {
                assert!(lo <= hi, "pivot must be <= highest_bit");
            }
        }
    }
}

#[test]
fn zero_predicates_are_mutually_consistent() {
    // is_zero ⇔ count_ones()==0 ⇔ pivot()==None ⇔ highest_bit()==None
    for &len in BITVEC_LENS {
        for seed in 0..32u64 {
            let mut rng = Rng::new(seed ^ 0x84 ^ (len as u64) << 8);
            // sweep densities including 0 so genuine zero vectors appear
            let density = (seed % 5) as u32 * 6;
            let v = gen_bitvec(&mut rng, len, density);
            let z = v.is_zero();
            assert_eq!(z, v.count_ones() == 0, "is_zero vs count_ones");
            assert_eq!(z, v.pivot().is_none(), "is_zero vs pivot");
            assert_eq!(z, v.highest_bit().is_none(), "is_zero vs highest_bit");
        }
    }
}

#[test]
fn singleton_has_exactly_one_bit() {
    for &len in BITVEC_LENS {
        if len == 0 {
            continue; // no valid bit index
        }
        for bit in [0, len / 2, len - 1] {
            let v = BitVec::singleton(len, bit);
            assert_eq!(
                v.count_ones(),
                1,
                "singleton popcount (len={len}, bit={bit})"
            );
            assert!(v.get(bit));
            assert_eq!(v.pivot(), Some(bit));
            assert_eq!(v.highest_bit(), Some(bit));
            assert!(!v.is_zero());
            let ones: Vec<usize> = v.ones().collect();
            assert_eq!(ones, vec![bit]);
        }
    }
}

