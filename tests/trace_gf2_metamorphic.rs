// persistence_pairs — structural well-formedness
// ---------------------------------------------------------------------------

#[test]
fn persistence_pairs_are_structurally_well_formed() {
    for &(rows, cols, density) in MATRIX_CASES {
        for seed in 0..10u64 {
            let mut rng = Rng::new(seed ^ 0x7A7A ^ (rows as u64) << 16 ^ (cols as u64));
            let m = gen_matrix(&mut rng, rows, cols, density);
            let reduced = m.reduce();
            let pp = reduced.persistence_pairs();

            let mut births = BTreeSet::new();
            let mut deaths = BTreeSet::new();
            for &(birth, death) in &pp.pairs {
                assert!(birth < rows, "pair birth {birth} >= rows {rows}");
                assert!(death < cols, "pair death {death} >= cols {cols}");
                assert!(births.insert(birth), "duplicate birth row {birth}");
                assert!(deaths.insert(death), "duplicate death col {death}");
            }
            for &u in &pp.unpaired {
                assert!(u < cols, "unpaired index {u} >= cols {cols}");
                assert!(
                    reduced.matrix.column(u).is_zero(),
                    "unpaired column {u} is not zero"
                );
            }
        }
    }
}

#[test]
fn persistence_pair_count_equals_rank() {
    // Every non-zero reduced column contributes exactly one (pivot, col) pair.
    for &(rows, cols, density) in MATRIX_CASES {
        for seed in 0..10u64 {
            let mut rng = Rng::new(seed ^ 0x6B6B ^ (rows as u64) << 16 ^ (cols as u64));
            let m = gen_matrix(&mut rng, rows, cols, density);
            let r = rank(&m);
            let reduced = m.reduce();
            let pp = reduced.persistence_pairs();
            assert_eq!(
                pp.pairs.len(),
                r,
                "pair count != rank ({rows}x{cols}, seed={seed})"
            );
        }
    }
}

#[test]
fn persistence_pairs_never_panic_on_extreme_shapes() {
    // Stress the rows/cols dimension split that previously caused an
    // out-of-bounds index on non-square matrices.
    let shapes = [
        (1, 64),
        (64, 1),
        (3, 200),
        (200, 3),
        (65, 65),
        (0, 4),
        (4, 0),
    ];
    for &(rows, cols) in &shapes {
        for seed in 0..6u64 {
            let mut rng = Rng::new(seed ^ 0xBAD0 ^ (rows as u64) << 16 ^ (cols as u64));
            let m = gen_matrix(&mut rng, rows, cols, 12);
            let reduced = m.reduce();
            let pp = reduced.persistence_pairs();
            // Total accounted features cannot exceed the column count.
            assert!(
                pp.pairs.len() + pp.unpaired.len() <= cols,
                "feature count exceeds cols for {rows}x{cols}"
            );
        }
    }
}

