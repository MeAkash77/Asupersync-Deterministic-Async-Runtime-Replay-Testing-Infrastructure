#[test]
fn flip_is_an_involution_per_bit() {
    for &len in BITVEC_LENS {
        if len == 0 {
            continue;
        }
        for seed in 0..16u64 {
            let mut rng = Rng::new(seed ^ 0xC1 ^ (len as u64) << 8);
            let original = gen_bitvec(&mut rng, len, 24);
            let mut v = original.clone();
            let i = rng.below(len);
            v.flip(i);
            v.flip(i);
            assert_eq!(v, original, "double flip != identity (len={len}, bit={i})");
        }
    }
}

#[test]
fn flip_toggles_exactly_the_target_bit() {
    for &len in BITVEC_LENS {
        if len == 0 {
            continue;
        }
        for seed in 0..16u64 {
            let mut rng = Rng::new(seed ^ 0xD2 ^ (len as u64) << 8);
            let mut v = gen_bitvec(&mut rng, len, 24);
            let i = rng.below(len);
            let before = v.get(i);
            let others_before: Vec<bool> = (0..len).map(|k| v.get(k)).collect();
            v.flip(i);
            assert_eq!(v.get(i), !before, "flip didn't toggle bit {i}");
            for k in 0..len {
                if k != i {
                    assert_eq!(v.get(k), others_before[k], "flip disturbed bit {k}");
                }
            }
        }
    }
}

#[test]
fn set_and_clear_are_idempotent_and_opposite() {
    for &len in BITVEC_LENS {
        if len == 0 {
            continue;
        }
        for seed in 0..16u64 {
            let mut rng = Rng::new(seed ^ 0xE3 ^ (len as u64) << 8);
            let mut v = gen_bitvec(&mut rng, len, 24);
            let i = rng.below(len);

            v.set(i);
            let after_set = v.clone();
            v.set(i);
            assert_eq!(v, after_set, "set is not idempotent");
            assert!(v.get(i));

            v.clear(i);
            let after_clear = v.clone();
            v.clear(i);
            assert_eq!(v, after_clear, "clear is not idempotent");
            assert!(!v.get(i));
        }
    }
}

