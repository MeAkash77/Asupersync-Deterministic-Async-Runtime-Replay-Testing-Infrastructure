//! Metamorphic conformance harness for `asupersync::trace::gf2`.
//!
//! `trace::gf2` is the GF(2) bitset linear algebra under the persistent
//! homology pipeline (`trace::scoring`, `trace::geodesic`). It has no
//! observable "correct answer" for an arbitrary input matrix — the *oracle
//! problem* — but it does satisfy a dense web of algebraic relations that any
//! correct implementation must preserve. This harness pins those relations.
//!
//! ## Metamorphic relations covered
//!
//! `BitVec` as a vector in the GF(2) vector space `GF(2)^n`:
//! - XOR is an abelian group: closure, commutativity, associativity,
//!   identity (`zeros`), self-inverse (`a^a == 0`), involution (`(a^b)^b==a`).
//! - `xor` (functional) and `xor_assign` (in-place) agree.
//! - Parity: `popcount(a^b) ≡ popcount(a)+popcount(b) (mod 2)`.
//! - Hamming bounds: `||a|-|b|| ≤ popcount(a^b) ≤ |a|+|b|`.
//! - `set`/`clear`/`flip` idempotence and involution.
//! - `pivot == min(ones())`, `highest_bit == max(ones())`,
//!   `count_ones == ones().count()`, `ones()` strictly ascending and bounded.
//! - The four-way equivalence `is_zero ⇔ popcount 0 ⇔ pivot None ⇔ highest None`.
//!
//! `BoundaryMatrix::reduce` (standard left-to-right column reduction):
//! - Determinism (same input → identical reduced matrix).
//! - Dimension preservation.
//! - Pivot uniqueness: distinct non-zero reduced columns have distinct pivots.
//! - Idempotence: reducing an already-reduced matrix changes no pivot.
//! - `pivot_map` round-trips with `column_pivot`.
//! - Rank (= count of non-zero reduced columns) is invariant under column
//!   permutation and under elementary column XORs — because `reduce` is built
//!   from exactly those operations.
//! - Zero columns survive reduction as zero columns.
//! - Duplicating a non-zero column does not raise rank.
//!
//! `persistence_pairs`:
//! - structural well-formedness (in-range indices, unique births, unique
//!   deaths), pair count equals rank, no panic on non-square matrices.
//!
//! All inputs are generated by a deterministic in-test LCG so failures are
//! reproducible from the printed seed; no `proptest` dependency.

#![allow(clippy::needless_range_loop)]
#![allow(clippy::too_many_lines)]

use std::collections::BTreeSet;

use asupersync::trace::{BitVec, BoundaryMatrix};

// ---------------------------------------------------------------------------
// Deterministic generators
// ---------------------------------------------------------------------------

/// SplitMix64 — a small, well-distributed deterministic PRNG. Reproducible
/// across platforms; a failing case is fully described by its seed.
struct Rng(u64);

impl Rng {
    fn new(seed: u64) -> Self {
        Self(seed.wrapping_add(0x9E37_79B9_7F4A_7C15))
    }

    fn next_u64(&mut self) -> u64 {
        self.0 = self.0.wrapping_add(0x9E37_79B9_7F4A_7C15);
        let mut z = self.0;
        z = (z ^ (z >> 30)).wrapping_mul(0xBF58_476D_1CE4_E5B9);
        z = (z ^ (z >> 27)).wrapping_mul(0x94D0_49BB_1331_11EB);
        z ^ (z >> 31)
    }

    /// Uniform integer in `[0, n)`. `n` must be non-zero.
    fn below(&mut self, n: usize) -> usize {
        (self.next_u64() % n as u64) as usize
    }

    fn bool_with(&mut self, ones_in_64: u32) -> bool {
        (self.next_u64() & 63) < u64::from(ones_in_64)
    }
}

/// Build a `BitVec` of `len` bits, each set with probability `density/64`.
fn gen_bitvec(rng: &mut Rng, len: usize, density: u32) -> BitVec {
    let mut v = BitVec::zeros(len);
    for i in 0..len {
        if rng.bool_with(density) {
            v.set(i);
        }
    }
    v
}

/// Build a `rows x cols` boundary matrix with random entries.
fn gen_matrix(rng: &mut Rng, rows: usize, cols: usize, density: u32) -> BoundaryMatrix {
    let mut m = BoundaryMatrix::zeros(rows, cols);
    for j in 0..cols {
        for i in 0..rows {
            if rng.bool_with(density) {
                m.set(i, j);
            }
        }
    }
    m
}

/// Sizes / seeds swept by the matrix-level relations. Mixes square, wide,
/// and tall shapes plus assorted bit densities.
const MATRIX_CASES: &[(usize, usize, u32)] = &[
    (1, 1, 32),
    (3, 3, 24),
    (5, 5, 20),
    (8, 8, 16),
    (16, 16, 12),
    (32, 24, 10),
    (24, 40, 8),
    (70, 50, 6), // crosses the 64-bit word boundary
    (50, 70, 6),
    (128, 64, 4),
];

const BITVEC_LENS: &[usize] = &[0, 1, 7, 63, 64, 65, 127, 128, 200, 500];

// ---------------------------------------------------------------------------
// Small helpers shared across relations
// ---------------------------------------------------------------------------

fn rank(m: &BoundaryMatrix) -> usize {
    let reduced = m.reduce();
    (0..reduced.matrix.cols())
        .filter(|&j| reduced.matrix.column_pivot(j).is_some())
        .count()
}

fn columns(m: &BoundaryMatrix) -> Vec<BitVec> {
    (0..m.cols()).map(|j| m.column(j).clone()).collect()
}

// ---------------------------------------------------------------------------
// BitVec — GF(2) vector space axioms
// ---------------------------------------------------------------------------

#[test]
fn xor_is_commutative() {
    for &len in BITVEC_LENS {
        for seed in 0..24u64 {
            let mut rng = Rng::new(seed ^ (len as u64) << 8);
            let a = gen_bitvec(&mut rng, len, 24);
            let b = gen_bitvec(&mut rng, len, 24);
            assert_eq!(
                a.xor(&b),
                b.xor(&a),
                "xor not commutative (len={len}, seed={seed})"
            );
        }
    }
}

#[test]
fn xor_is_associative() {
    for &len in BITVEC_LENS {
        for seed in 0..24u64 {
            let mut rng = Rng::new(seed ^ 0xA5 ^ (len as u64) << 8);
            let a = gen_bitvec(&mut rng, len, 20);
            let b = gen_bitvec(&mut rng, len, 20);
            let c = gen_bitvec(&mut rng, len, 20);
            let left = a.xor(&b).xor(&c);
            let right = a.xor(&b.xor(&c));
            assert_eq!(left, right, "xor not associative (len={len}, seed={seed})");
        }
    }
}

#[test]
fn xor_zero_is_identity_and_self_is_inverse() {
    for &len in BITVEC_LENS {
        for seed in 0..24u64 {
            let mut rng = Rng::new(seed ^ 0x3C ^ (len as u64) << 8);
            let a = gen_bitvec(&mut rng, len, 24);
            let zero = BitVec::zeros(len);

            // identity: a ^ 0 == a
            assert_eq!(a.xor(&zero), a, "xor identity (len={len}, seed={seed})");
            // self-inverse: a ^ a == 0
            let self_xor = a.xor(&a);
            assert!(
                self_xor.is_zero(),
                "a^a must be zero (len={len}, seed={seed})"
            );
            assert_eq!(self_xor, zero);
        }
    }
}

#[test]
fn xor_is_an_involution() {
    // (a ^ b) ^ b == a — flipping the same vector twice is the identity.
    for &len in BITVEC_LENS {
        for seed in 0..32u64 {
            let mut rng = Rng::new(seed ^ 0x7E ^ (len as u64) << 8);
            let a = gen_bitvec(&mut rng, len, 22);
            let b = gen_bitvec(&mut rng, len, 22);
            assert_eq!(
                a.xor(&b).xor(&b),
                a,
                "xor involution failed (len={len}, seed={seed})"
            );
        }
    }
}

#[test]
fn xor_assign_agrees_with_functional_xor() {
    for &len in BITVEC_LENS {
        for seed in 0..24u64 {
            let mut rng = Rng::new(seed ^ 0x11 ^ (len as u64) << 8);
            let a = gen_bitvec(&mut rng, len, 20);
            let b = gen_bitvec(&mut rng, len, 20);
            let functional = a.xor(&b);
            let mut in_place = a.clone();
            in_place.xor_assign(&b);
            assert_eq!(
                functional, in_place,
                "xor / xor_assign disagree (len={len}, seed={seed})"
            );
        }
    }
}

